- (BOOL)application:(UIApplication *)application didFinishLaunchingWithOptions:(NSDictionary *)launchOptions {
    // Override point for customization after application launch.
    //添加初始化APNs代码
    //Required
    //notice: 3.0.0及以后版本注册可以这样写，也可以继续用之前的注册方式
    JPUSHRegisterEntity * entity = [[JPUSHRegisterEntity alloc] init];
    entity.types = JPAuthorizationOptionAlert|JPAuthorizationOptionBadge|JPAuthorizationOptionSound;
    if ([[UIDevice currentDevice].systemVersion floatValue] >= 8.0) {
        // 可以添加自定义categories
        // NSSet<UNNotificationCategory *> *categories for iOS10 or later
        // NSSet<UIUserNotificationCategory *> *categories for iOS8 and iOS9
    }
    [JPUSHService registerForRemoteNotificationConfig:entity delegate:self];
    //添加初始化JPush代码
    // Optional
    // 获取IDFA
    // 如需使用IDFA功能请添加此代码并在初始化方法的advertisingIdentifier参数中填写对应值
    NSString *advertisingId = [[[ASIdentifierManager sharedManager] advertisingIdentifier] UUIDString];
    
    // Required
    // init Push
    // notice: 2.1.5版本的SDK新增的注册方法，改成可上报IDFA，如果没有使用IDFA直接传nil
    // 如需继续使用pushConfig.plist文件声明appKey等配置内容，请依旧使用[JPUSHService setupWithOption:launchOptions]方式初始化。
    NSString * appKey = @"23aa10cb68a80a933c1e3e50";
    NSString * channel = @"App Store";
    NSString * isProduction = @"0";//0 (默认值)表示采用的是开发证书，1 表示采用生产证书发布应用。
    if (ENVIRONMENT == 1) {
        isProduction = @"0";
    }else{
        isProduction = @"1";
    }
    [JPUSHService setupWithOption:launchOptions appKey:appKey
                          channel:channel
                 apsForProduction:isProduction
            advertisingIdentifier:advertisingId];
    //2.1.9版本新增获取registration id block接口。
    [JPUSHService registrationIDCompletionHandler:^(int resCode, NSString *registrationID) {
        if(resCode == 0){
            NSLog(@"registrationID获取成功：%@",registrationID);
            
        }
        else{
            NSLog(@"registrationID获取失败，code：%d",resCode);
        }
    }];
    //添加消息通知
    [self addNotificationMethods];
    return YES;
}

- (void)addNotificationMethods
{
    //跳转到登录界面
    [[NSNotificationCenter defaultCenter]addObserver:self selector:@selector(jumpToLogin) name:@"jumpToLogin" object:nil];
    [[NSNotificationCenter defaultCenter] addObserver:self selector:@selector(jumpToFirstLogin) name:@"jumpToFirstLogin" object:nil];
    [[NSNotificationCenter defaultCenter] addObserver:self selector:@selector(jumpToMainTB) name:@"jumpToMainTB" object:nil];
}
- (void)jumpToLogin
{
    UIStoryboard * mainStroyboard = [UIStoryboard storyboardWithName:@"Main" bundle:[NSBundle mainBundle]];
    UINavigationController * login = (UINavigationController *)[mainStroyboard instantiateViewControllerWithIdentifier:@"login"];
    self.window.rootViewController = login;
}
- (void)jumpToFirstLogin{
    UIStoryboard * mainStroyboard = [UIStoryboard storyboardWithName:@"Main" bundle:[NSBundle mainBundle]];
    UINavigationController * checkBoxNC = (UINavigationController *)[mainStroyboard instantiateViewControllerWithIdentifier:@"firstlogin"];
    self.window.rootViewController = checkBoxNC;
}
- (void)jumpToMainTB{
    UIStoryboard * mainStroyboard = [UIStoryboard storyboardWithName:@"Main" bundle:[NSBundle mainBundle]];
    UITabBarController * mainTB = (UITabBarController *)[mainStroyboard instantiateViewControllerWithIdentifier:@"maintb"];
    self.window.rootViewController = mainTB;
}

